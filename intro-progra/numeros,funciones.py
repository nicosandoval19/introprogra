import math

print(round(1.3))#redondea al entero más cercano
print(round(1.7))
print(abs(-5))#valor absoluto
print(abs(5))

print(math.ceil(1.3))#redondea hacia arriba
print(math.floor(1.7))#redondea hacia abajo 
print(math.isnan(23))#verifica si un número es NaN (Not a Number)
print(math.pow(2,3))#potencia
print(math.sqrt(16))#raíz cuadrada