print(5 > 3)  # True
print(5 < 3)  # False
print(5 == 5)  # True
print(5 != 3)  # True
print(5 >= 5)  # True
print(5 <= 3)  # False
print("hola" == "hola")  # True
print("hola" != "Hola")  # True
print("hola" > "Hola")  # True (comparación lexicográfica, 'h' tiene un valor ASCII mayor que 'H')
print("hola" < "Hola")  # False (comparación lexicográfica, 'h' tiene un valor ASCII mayor que 'H')